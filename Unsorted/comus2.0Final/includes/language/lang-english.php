function tsl($co)
{
$tyes = GTGP_YES;
$tno  = GTGP_NO;
if($co == "Yes") { return $tyes;}
if($co == "No")  { return $tno;}
}

function tslb($co)
{
$tyes = GTGP_YES1;
$tno  = GTGP_NO1;
if($co == "Yes") { return $tyes;}
if($co == "No")  { return $tno;}
}

function tslbl($co)
{
$email  = GTGP_ADMIN_BL_EMAIL;
$domain = GTGP_ADMIN_BL_DOMAIN;
$ip = GTGP_ADMIN_BL_IP;
if($co == "email")  { return $email;}
if($co == "domain") { return $domain;}
if($co == "ip")     { return $ip;}
}

