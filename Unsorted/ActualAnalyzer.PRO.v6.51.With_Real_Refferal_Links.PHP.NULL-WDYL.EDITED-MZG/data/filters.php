Add below this block the IP addresses for which it is necessary to block the
counting. For example:

$ip_filter[]='192.168.0.1';
$ip_filter[]='192.168.0.2';
$ip_filter[]='192.168.0.3';

will block the count for users with IP = 192.168.0.1 or 192.168.0.2 or
192.168.0.3

It is accessible to set the range of IP addresses for which it is necessary to
block the counting. For example:

$ip_filter[]='192.168.0.1-192.168.0.3';

will block the count for users with IP = 192.168.0.1 or 192.168.0.2 or
192.168.0.3
------------------------------------------------------------------------------*/







