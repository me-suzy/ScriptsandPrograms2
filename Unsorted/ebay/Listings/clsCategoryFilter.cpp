bool clsCategoryFilter::AllowedCategory(int i) const
{
	if (i >= mMaxCategories)
		return true;

	const Category& category = mCategories[i];

	vector<int>::const_iterator begin = mAllowedCategory.begin();
	vector<int>::const_iterator end = mAllowedCategory.end();

	if (   (end != find(begin, end, i))
		|| (category.mCategory1 && end != find(begin, end, category.mCategory1))
		|| (category.mCategory2 && end != find(begin, end, category.mCategory2))
		|| (category.mCategory3 && end != find(begin, end, category.mCategory3))
		|| (category.mCategory4 && end != find(begin, end, category.mCategory4)) )
		return true;
	else
		return false;
}

