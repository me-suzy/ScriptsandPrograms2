/*
 * ebay_locale.sql
 * is the place where we keep info about a locale
*/

drop table ebay_locale;

create table ebay_locale
 (
	LOCALE_ID				NUMBER(5)
		constraint		locale_id_nn
			not null,
	COUNTRY_ID				NUMBER(10)
		constraint		country_id_nn
			not null,
	DATE_FORMAT_SHORT		VARCHAR2(10)
		constraint		date_format_short_nn
			not null,
	DATE_FORMAT_MEDIUM		VARCHAR2(15)
		constraint		date_format_medium_nn
			not null,
	DATE_FORMAT_LONG		VARCHAR2(20)
		constraint		date_format_long_nn
			not null,
	TIME_FORMAT_SHORT		VARCHAR2(10)
		constraint		time_format_short_nn
			not null,
	TIME_FORMAT_MEDIUM		VARCHAR2(15)
		constraint		time_format_medium_nn
			not null,
	TIME_FORMAT_LONG		VARCHAR2(20)
		constraint		time_format_short_nn
			not null,
	POSITIVE_AMOUNT_FORMAT	VARCHAR2(15)
		constraint		positive_amount_format_nn
			not null,
	NEGATIVE_AMOUNT_FORMAT	VARCHAR2(15)
		constraint		negative_amount_format_nn
			not null,
	DECIMAL_SYMBOL			VARCHAR2(1)
		constraint		decimal_symbol_nn
			not null,
	GROUPING_SYMBOL			VARCHAR2(1)
		constraint		grouping_symbol_nn
			not null,
	DIGITS_AFTER_DECIMAL	NUMBER(1)
		constraint		digits_after_decimal_nn
			not null,
	DIGITS_IN_GROUP			NUMBER(1)
		constraint		digits_in_group_nn
			not null,
	NEGATIVE_NUMBER_FORMAT  VARCHAR2(15)
		constraint		negative_number_format_nn
			not null
)

alter table ebay_locale
	add constraint			locale_pk
		primary key		(locale_id);


